#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	t_list *current_node;
	int	count;

	current_node = lst;
	count = 0;
	while (current_node != NULL)
	{
		current_node = current_node->next;
		count++;
	}
	return (count);
}
