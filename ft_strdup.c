#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*dest;
	int		i;
	int		length;

	i = 0;
	length = ft_strlen(s);
	dest = malloc((length + 1) * sizeof(char));
	if (dest == NULL)
		return (NULL);
	while (s[i] != '\0')
	{
		dest[i] = s[i];
		i++;
	}
	dest[length] = '\0';
	return (dest);
}
